public class BitOperations {

  public static void main(String[] args) {
    
    int a = 7;
    int b = a << 1;
    int c = a << 2;
    int d = a << 3;
    int e = a << 4;
    long z = (long) a;
    long w = z << 30;

    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
    System.out.println(w);
  }
}
